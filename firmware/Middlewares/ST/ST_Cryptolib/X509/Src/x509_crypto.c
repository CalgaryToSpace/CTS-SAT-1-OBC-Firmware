/**
* @brief  Check whether "parent" signed "child"
* @param[in]  *parent pointer to the parsed x509 certificate of the supposed issuer of child
* @param[in]  *child  pointer to the parsed x509 certificate of the certificated supposedly issued by parent
* @return Validity of the parentship
* @retval 1 child was signed by parent
* @retval -1 child was \b not signed by parent
*/
int32_t verifyCertSignature(const intCert_stt *parent, const intCert_stt *child)
{
  uint8_t digest[512 / 8];
  int32_t digestSize = 0;

  /* Compute hash */
  if (child->SignatureAlgorithm == SIG_ECDSA_SHA1)
  {
    HASHctx_stt ctx;
    ctx.mFlags = E_HASH_DEFAULT;
    ctx.mTagSize = CRL_SHA1_SIZE; 
    (void)SHA1_Init(&ctx);
    (void)SHA1_Append(&ctx, child->tbs, child->tbsSize);
    (void)SHA1_Finish(&ctx, digest, &digestSize);
  }
  else if (child->SignatureAlgorithm == SIG_ECDSA_SHA224)
  {
    HASHctx_stt ctx;
    ctx.mFlags = E_HASH_DEFAULT;
    ctx.mTagSize = CRL_SHA224_SIZE; 
    (void)SHA224_Init(&ctx);
    (void)SHA224_Append(&ctx, child->tbs, child->tbsSize);
    (void)SHA224_Finish(&ctx, digest, &digestSize);
  }	
  else if (child->SignatureAlgorithm == SIG_ECDSA_SHA256)
  {
    HASHctx_stt ctx;
    ctx.mFlags = E_HASH_DEFAULT;
    ctx.mTagSize = CRL_SHA256_SIZE;
    (void)SHA256_Init(&ctx);
    (void)SHA256_Append(&ctx, child->tbs, child->tbsSize);
    (void)SHA256_Finish(&ctx, digest, &digestSize);
  }
  else if (child->SignatureAlgorithm == SIG_ECDSA_SHA384)
  {
    HASHLctx_stt ctx;    
    ctx.mFlags = E_HASH_DEFAULT;
    ctx.mTagSize = CRL_SHA384_SIZE;
    (void)SHA384_Init(&ctx);
    (void)SHA384_Append(&ctx, child->tbs, child->tbsSize);
    (void)SHA384_Finish(&ctx, digest, &digestSize);
  }
  else if (child->SignatureAlgorithm == SIG_ECDSA_SHA512)
  {
    HASHLctx_stt ctx;
    ctx.mFlags = E_HASH_DEFAULT;
    ctx.mTagSize = CRL_SHA512_SIZE;
    (void)SHA512_Init(&ctx);
    (void)SHA512_Append(&ctx, child->tbs, child->tbsSize);
    (void)SHA512_Finish(&ctx, digest, &digestSize);
  }
  else
  {
    /* We should never be here because if we are then signatureAlgorithm was not allowed but this is checked during isValid */
    /* In any case returning -1 is safe */
    return (-1);
  }

  return(verifySignature(parent, digest, digestSize, child->Sign.pR, child->Sign.rSize, child->Sign.pS, child->Sign.sSize));
}

/**
* @brief  Verify the signature of a digest with a public key extracted from the certificate
* @param[in]  *cert pointer to the parsed x509 certificate containing the public key to be used for the signature verification
* @param[in]  *digest the digest of the message to be verified
* @param[in]   digestSize size in bytes of the digest of the message to be verified
* @param[in]   *signatureR the R value of the signature to be verified
* @param[in]   *signatureRsize the size in bytes of signatureR
* @param[in]   *signatureS the S value of the signature to be verified
* @param[in]   *signatureSsize the size in bytes of signatureS
* @return Validity of the parentship
* @retval 1 child was signed by parent
* @retval -1 child was \b not signed by parent
*/
int32_t verifySignature(const intCert_stt *cert,
  const uint8_t *digest,
  int32_t digestSize,
  const uint8_t *signatureR,
  int32_t signatureRsize,
  const uint8_t *signatureS,
  int32_t signatureSsize)
{
  uint8_t preallocated_buffer[2176]; /* buffer required for internal allocation of memory */
  membuf_stt mb_st; /* structure that will contain the preallocated buffer */
  EC_stt ECparams_st; /*  structure that will keep the Elliptic Curve parameters*/
  ECpoint_stt *PubKey_st = NULL; /* Point that will contain the public key */
  ECDSAsignature_stt *Signature_st = NULL; /* Structure that will contain the ECDSA signature */
  ECDSAverifyCtx_stt verctx_st; /* Structure context used to call the ECDSAverify */
  int32_t retval;

  mb_st.mSize = (uint16_t)sizeof(preallocated_buffer);
  mb_st.pmBuf = preallocated_buffer;
  mb_st.mUsed = 0;

  /* Now import public key */
  if ((cert->EllipticCurve == EC_P256) && (cert->PubKey.fsize == (256 / 8)))
  {
    ECparams_st.pmA = P_256_a;
    ECparams_st.pmB = P_256_b;
    ECparams_st.pmP = P_256_p;
    ECparams_st.pmN = P_256_n;
    ECparams_st.pmGx = P_256_Gx;
    ECparams_st.pmGy = P_256_Gy;
    ECparams_st.mAsize = (int32_t)sizeof(P_256_a);
    ECparams_st.mBsize = (int32_t)sizeof(P_256_b);
    ECparams_st.mPsize = (int32_t)sizeof(P_256_p);
    ECparams_st.mNsize = (int32_t)sizeof(P_256_n);
    ECparams_st.mGxsize = (int32_t)sizeof(P_256_Gx);
    ECparams_st.mGysize = (int32_t)sizeof(P_256_Gy);
  }
  else if ((cert->EllipticCurve == EC_P384) && (cert->PubKey.fsize == (384 / 8)))
  {
    ECparams_st.pmA = P_384_a;
    ECparams_st.pmB = P_384_b;
    ECparams_st.pmP = P_384_p;
    ECparams_st.pmN = P_384_n;
    ECparams_st.pmGx = P_384_Gx;
    ECparams_st.pmGy = P_384_Gy;
    ECparams_st.mAsize = (int32_t)sizeof(P_384_a);
    ECparams_st.mBsize = (int32_t)sizeof(P_384_b);
    ECparams_st.mPsize = (int32_t)sizeof(P_384_p);
    ECparams_st.mNsize = (int32_t)sizeof(P_384_n);
    ECparams_st.mGxsize = (int32_t)sizeof(P_384_Gx);
    ECparams_st.mGysize = (int32_t)sizeof(P_384_Gy);
  }
  else if ((cert->EllipticCurve == EC_bp256r1) && (cert->PubKey.fsize == (256 / 8)))
  {
    ECparams_st.pmA = brainpoolP256r1_a;
    ECparams_st.pmB = brainpoolP256r1_b;
    ECparams_st.pmP = brainpoolP256r1_p;
    ECparams_st.pmN = brainpoolP256r1_n;
    ECparams_st.pmGx = brainpoolP256r1_Gx;
    ECparams_st.pmGy = brainpoolP256r1_Gy;
    ECparams_st.mAsize = (int32_t)sizeof(brainpoolP256r1_a);
    ECparams_st.mBsize = (int32_t)sizeof(brainpoolP256r1_b);
    ECparams_st.mPsize = (int32_t)sizeof(brainpoolP256r1_p);
    ECparams_st.mNsize = (int32_t)sizeof(brainpoolP256r1_n);
    ECparams_st.mGxsize = (int32_t)sizeof(brainpoolP256r1_Gx);
    ECparams_st.mGysize = (int32_t)sizeof(brainpoolP256r1_Gy);
  }
  else if ((cert->EllipticCurve == EC_bp384r1) && (cert->PubKey.fsize == (384 / 8)))
  {
    ECparams_st.pmA = brainpoolP384r1_a;
    ECparams_st.pmB = brainpoolP384r1_b;
    ECparams_st.pmP = brainpoolP384r1_p;
    ECparams_st.pmN = brainpoolP384r1_n;
    ECparams_st.pmGx = brainpoolP384r1_Gx;
    ECparams_st.pmGy = brainpoolP384r1_Gy;
    ECparams_st.mAsize = (int32_t)sizeof(brainpoolP384r1_a);
    ECparams_st.mBsize = (int32_t)sizeof(brainpoolP384r1_b);
    ECparams_st.mPsize = (int32_t)sizeof(brainpoolP384r1_p);
    ECparams_st.mNsize = (int32_t)sizeof(brainpoolP384r1_n);
    ECparams_st.mGxsize = (int32_t)sizeof(brainpoolP384r1_Gx);
    ECparams_st.mGysize = (int32_t)sizeof(brainpoolP384r1_Gy);
  }
  else
  {
    /* If the curve is not valid in the stsafe context or if the field size doesn't match the curve then we return invalid */
    return(-1);
  }

  if (ECCinitEC(&ECparams_st, &mb_st) == ECC_SUCCESS)
  {
    if (ECCinitPoint(&PubKey_st, &ECparams_st, &mb_st) == ECC_SUCCESS)
    {
      retval = ECCsetPointCoordinate(PubKey_st, E_ECC_POINT_COORDINATE_X, cert->PubKey.pX, cert->PubKey.fsize);
      retval += ECCsetPointCoordinate(PubKey_st, E_ECC_POINT_COORDINATE_Y, cert->PubKey.pY, cert->PubKey.fsize);
      retval += ECCvalidatePubKey(PubKey_st, &ECparams_st, &mb_st);
      if (retval == ECC_SUCCESS)
      {
        /* If ECCValidatePubkey had enough memory, this won't fail, so don't check */
        retval = ECDSAinitSign(&Signature_st, &ECparams_st, &mb_st);

        retval += ECDSAsetSignature(Signature_st, E_ECDSA_SIGNATURE_R_VALUE, signatureR, signatureRsize);
        retval += ECDSAsetSignature(Signature_st, E_ECDSA_SIGNATURE_S_VALUE, signatureS, signatureSsize);
        verctx_st.pmEC = &ECparams_st;
        verctx_st.pmPubKey = PubKey_st;
        retval += ECDSAverify(digest, digestSize, Signature_st, &verctx_st, &mb_st);
        if (retval == SIGNATURE_VALID)
        {
          return (1);
        }
      }
    }
  }

  return(-1);
}
